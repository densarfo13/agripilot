#!/usr/bin/env node
/**
 * bake-brand-mark.mjs \u2014 read the canonical premium-logo asset
 * and emit src/brand/farrowayMarkDataUrl.js as a single
 * `export const FARROWAY_MARK_DATA_URL = 'data:image/...;base64,...'`
 * line.
 *
 * Why: the deploy pipeline keeps dropping binary assets between
 * local and the build container (Rollup `Could not resolve`,
 * runtime 404). Baking the brand image into a committed JS
 * file removes the binary from the build pipeline entirely \u2014
 * the brand asset travels with the JS bundle.
 *
 * Source resolution (premium-logo migration):
 *   1. public/icons/logo-premium-1024.png  \u2014 preferred raster
 *   2. public/icons/logo-premium.svg       \u2014 vector fallback
 *   3. public/icons/logo-premium.png       \u2014 generic raster
 * The first existing file wins. The MIME type is derived from
 * the chosen extension. Old "farroway-mark.jpg" path is no
 * longer referenced.
 *
 * Re-run after dropping a new logo asset into /public/icons/:
 *   node scripts/bake-brand-mark.mjs
 *
 * Idempotent. Writes to src/brand/farrowayMarkDataUrl.js.
 */

import fs from 'node:fs';
import path from 'node:path';

const CANDIDATES = [
  // Prefer the highest-quality raster the user dropped in.
  // The JPG variant is what the brand team supplied as the
  // canonical premium logo; PNG / SVG paths stay registered
  // for future swaps without changing this script.
  { file: 'public/icons/logo-premium-1024.jpg', mime: 'image/jpeg'    },
  { file: 'public/icons/logo-premium-1024.png', mime: 'image/png'     },
  { file: 'public/icons/logo-premium.jpg',      mime: 'image/jpeg'    },
  { file: 'public/icons/logo-premium.png',      mime: 'image/png'     },
  { file: 'public/icons/logo-premium.svg',      mime: 'image/svg+xml' },
];

let SRC = null;
let MIME = null;
for (const c of CANDIDATES) {
  const abs = path.resolve(c.file);
  if (fs.existsSync(abs)) { SRC = abs; MIME = c.mime; break; }
}

const OUT = path.resolve('src/brand/farrowayMarkDataUrl.js');

if (!SRC) {
  console.error(
    'bake-brand-mark: no source found. Drop the new premium logo at one of:\n' +
    CANDIDATES.map((c) => `  ${c.file}`).join('\n')
  );
  process.exit(1);
}

const bytes = fs.readFileSync(SRC);
// SVGs encode as utf-8 text, not base64; both work in img src
// but base64 is uniform across MIME types and survives more
// browsers. We base64 everything for consistency.
const base64 = bytes.toString('base64');
const dataUrl = `data:${MIME};base64,${base64}`;

const header = `/**
 * farrowayMarkDataUrl.js \u2014 generated by scripts/bake-brand-mark.mjs.
 * DO NOT EDIT BY HAND. Re-run the script when the source asset
 * (one of /public/icons/logo-premium-*.png / .svg) changes.
 *
 * Embedding the brand image as a base64 data URL guarantees it
 * ships with the JS bundle \u2014 the deploy pipeline can't drop a
 * binary file we never reference as one.
 *
 * Source: ${path.relative(process.cwd(), SRC).split(path.sep).join('/')}
 * MIME:   ${MIME}
 * Bytes:  ${bytes.length}
 * Base64: ${base64.length}
 */
`;

const body = `export const FARROWAY_MARK_DATA_URL = '${dataUrl}';
export default FARROWAY_MARK_DATA_URL;
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, header + body, 'utf8');

console.log(`bake-brand-mark: wrote ${OUT}`);
console.log(`  source bytes: ${bytes.length}`);
console.log(`  base64 chars: ${base64.length}`);

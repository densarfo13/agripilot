#!/usr/bin/env node
/**
 * bake-brand-mark.mjs — read public/icons/farroway-mark.jpg
 * and emit src/brand/farrowayMarkDataUrl.js as a single
 * `export const FARROWAY_MARK_DATA_URL = 'data:image/jpeg;base64,...'`
 * line.
 *
 * Why: the deploy pipeline keeps dropping binary assets between
 * local and the build container (Rollup `Could not resolve`,
 * runtime 404). Baking the JPG into a committed JS file removes
 * the binary from the build pipeline entirely — the brand asset
 * travels with the JS bundle.
 *
 * Re-run when the source JPG changes:
 *   node scripts/bake-brand-mark.mjs
 *
 * Idempotent. Reads `public/icons/farroway-mark.jpg` (78 KB →
 * ~104 KB base64 → ~78 KB after gzip) and writes a JS file at
 * `src/brand/farrowayMarkDataUrl.js`.
 */

import fs from 'node:fs';
import path from 'node:path';

const SRC = path.resolve('public/icons/farroway-mark.jpg');
const OUT = path.resolve('src/brand/farrowayMarkDataUrl.js');

if (!fs.existsSync(SRC)) {
  console.error(`bake-brand-mark: source missing at ${SRC}`);
  process.exit(1);
}

const bytes = fs.readFileSync(SRC);
const base64 = bytes.toString('base64');
const dataUrl = `data:image/jpeg;base64,${base64}`;

const header = `/**
 * farrowayMarkDataUrl.js — generated by scripts/bake-brand-mark.mjs.
 * DO NOT EDIT BY HAND. Re-run the script when the source JPG
 * (public/icons/farroway-mark.jpg) changes.
 *
 * Embedding the JPG as a base64 data URL guarantees the brand
 * asset ships with the JS bundle — the deploy pipeline can't
 * drop a binary file we never reference as one.
 *
 * Source: public/icons/farroway-mark.jpg
 * Bytes:  ${bytes.length}
 * Base64: ${base64.length}
 */
`;

const body = `export const FARROWAY_MARK_DATA_URL = '${dataUrl}';
export default FARROWAY_MARK_DATA_URL;
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, header + body, 'utf8');

console.log(`bake-brand-mark: wrote ${OUT}`);
console.log(`  source bytes: ${bytes.length}`);
console.log(`  base64 chars: ${base64.length}`);
